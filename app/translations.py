def get_translation(key, language='es'):
    """
    Obtém a tradução de uma chave no idioma especificado
    
    Args:
        key: Chave da tradução
        language: Código do idioma ('pt' ou 'es')
        
    Returns:
        str: Texto traduzido ou a chave se não encontrado
    """
    if language not in TRANSLATIONS:
        language = 'es'
    
    return TRANSLATIONS[language].get(key, key)


def translate_dict(data, language='es'):
    """
    Traduz todas as chaves de um dicionário
    
    Args:
        data: Dicionário com chaves para traduzir
        language: Código do idioma
        
    Returns:
        dict: Dicionário com valores traduzidos
    """
    return {key: get_translation(value, language) if isinstance(value, str) else value 
            for key, value in data.items()}
